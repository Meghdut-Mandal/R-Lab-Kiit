

a <- 3
b <- 4
c <- 5
s <- (a + b + c) / 2
areaTriangle <- (s * (s - a) * (s - b) * (s - c))^(.5)
cat("\nThe area of the triangle ", areaTriangle)

pie <- 3.141
radius <- 20
areaCircle <- pie * (radius)^2
cat("The area of the given circle is ", areaCircle)

side <- 14
areaSqure <- side^2
cat("The area of the given Squre is ", areaSqure)

rectSideA <- 3
rectSideB <- 4
areaRectangle <- rectSideA * rectSideB
cat("The area of the given rectangle is ", areaRectangle)
